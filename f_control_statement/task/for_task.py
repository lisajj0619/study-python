# 1~n까지 합 출력
message = '정수를 입력하세요: ' #입력받을 메세지
number = int(input(message)) #정수 입력

total = 0 #총합을 0으로 시작
for i in range(number): #입력받은 정수의 값만큼 반복
    total += i + 1 #입력받은 정수의 값만큼 합

print(total) #for문 바깥에서 출력하여 합만 나오게 함
