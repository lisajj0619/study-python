# 1~n까지 합 출력 - 강사님.ver
message1 = '1~n까지의 합' #타이틀
message2 = 'n: ' #정수를 입력 받기위한 타이틀
end = int(input(message1 + '\n' + message2)) #문자열 끼리 '+'로 이어준다. 정수n값을 입력 받음

total = 0 #총합은 0으로 시작
for i in range(end): #end에서 입력받은 정수만큼 반복해서 더하기
    total += i + 1 #입력받은 정수의 값만큼 합

print(total) #for문 바깥에서 출력하여 합만 나오게 함

