# '1,235,500'를 1235500으로 출력
data = '1,235,500' #변수에 문자열을 먼저 저장하기
result = '' #결과값에 비어있는 문자열을 저장
for i in data: #문자열의 각 문자를 반복
    if i != ',': #쉼표가 아닐 경우에
        result += i #결과값에 추가한다

print(result, type(result)) #이렇게 출력하면 문자열인 상태로 출력됨
result = int(result) #문자열을 정수형으로 형변환
print(result, type(result)) #정수로 형변환 됐는지 출력 및 확인
